  MX_RTC_Init();
  MX_I2C1_Init();
  /* USER CODE BEGIN 2 */
  ssd1306_Init(); // Turn on the OLED hardware
  ssd1306_Fill(Black); // Clear any old junk off the screen
  ssd1306_SetCursor(20, 0); // Position the text
  ssd1306_WriteString("DIGITAL CLOCK", Font_7x10);
  ssd1306_UpdateScreen(); // Actually push the data to the screen
  HAL_Delay(1000); // Wait a second so you can see the title
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {/* 1. Get current time and date from RTC */
	  HAL_RTC_GetTime(&hrtc, &sTime, RTC_FORMAT_BIN);
	  HAL_RTC_GetDate(&hrtc, &sDate, RTC_FORMAT_BIN); // MUST be called after GetTime!

	  /* 2. Format the time into a string */
	  sprintf(time_buffer, "%02d:%02d:%02d", sTime.Hours, sTime.Minutes, sTime.Seconds);

	  /* 3. Update the OLED Display */
	  ssd1306_SetCursor(20, 30); // Move below the title
	  ssd1306_WriteString(time_buffer, Font_11x18); // Large font for visibility
	  ssd1306_UpdateScreen();

	  HAL_Delay(500); // Refresh twice a second for a smooth look
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
